import ComposableArchitecture

@Reducer
struct FeedFeature {
    @ObservableState
    struct State: Equatable {}

    enum Action: BindableAction, Equatable {
        case binding(BindingAction<State>)
    }

    var body: some ReducerOf<Self> {
        BindingReducer()
        Reduce { state, action in
            switch action {
            case .binding:
                return .none
            }
        }
    }
}
